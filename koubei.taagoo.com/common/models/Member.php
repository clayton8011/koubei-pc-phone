<?php

namespace common\models;

use yii;
use yii\base\NotSupportedException;
use yii\web\IdentityInterface;

/**
 * This is the model class for table "{{%member}}".
 *
 * @property integer $id
 * @property integer $user_id
 * @property string $app_auth_token
 * @property string $app_refresh_token
 * @property string $expires_in
 * @property string $re_expires_in
 * @property string $auth_key
 * @property string $created_at
 */
class Member extends \yii\db\ActiveRecord implements IdentityInterface
{
    /**
     * @inheritdoc
     */
    public static function tableName()
    {
        return '{{%member}}';
    }

    /**
     * @inheritdoc
     */
    public function rules()
    {
        return [

        ];
    }

    /**
     * @inheritdoc
     */
    public function attributeLabels()
    {
        return [
            'id' => 'ID',
            'user_id' => '阿里ID',
            'app_auth_token' => 'app_auth_token 调用阿里接口的token',
            'app_refresh_token' => '刷新app_auth_token的token',
            'expires_in' => 'app_auth_token 过期时间（秒）',
            're_expires_in' => 'app_refresh_token 过期时间（秒）',
            'created_at' => '记录过期时间开始的时间',
        ];
    }

    /**
     * 场景设置
     * @return  {[type]} [description]
     * @version 1.0      2015-12-31T15:48:46+0800
     * @author cnzhangxl@foxmail.com
     */
    public function scenarios()
    {
        return [
            'create' => ["user_id", "app_auth_token", "app_refresh_token", "expires_in", "re_expires_in", "created_at"],
            'refresh_token' => ["app_auth_token", "app_refresh_token", "expires_in", "re_expires_in", "created_at"],
        ];
    }

    public function formName()
    {
        if (Yii::$app->request->post('appKey')) {
            return '';
        } else {
            return parent::formName(); // TODO: Change the autogenerated stub
        }

    }

    /**
     * @inheritdoc
     */
    public static function findIdentity($id)
    {
        return static::findOne(['id' => $id]);
    }

    /**
     * @inheritdoc
     */
    public static function findIdentityByAccessToken($token, $type = null)
    {
        throw new NotSupportedException('"findIdentityByAccessToken" is not implemented.');
    }

    /**
     * @inheritdoc
     */
    public function getId()
    {
        return $this->getPrimaryKey();
    }

    /**
     * @inheritdoc
     */
    public function getAuthKey()
    {
        return $this->auth_key;
    }

    /**
     * @inheritdoc
     */
    public function validateAuthKey($authKey)
    {
        return $this->getAuthKey() === $authKey;
    }

    /**
     * 刷新或添加登录的用户
     * @param $app_data object
     * @return boolean
     */
    public static function operationUser($app_data)
    {
        $user = Member::findOne(['user_id' => $app_data->user_id]);
        if ($user) {
            $user->scenario = 'refresh_token';
        } else {
            $user = new self();
            $user->scenario = 'create';
            $user->user_id = $app_data->user_id;
        }
        $user->app_auth_token = $app_data->app_auth_token;
        $user->app_refresh_token = $app_data->app_refresh_token;
        $user->expires_in = $app_data->expires_in;
        $user->re_expires_in = $app_data->re_expires_in;
        $user->created_at = $app_data->created_at;
        if($user->save()){
            return true;
        }else{
            Yii::error($user);
            return false;
        }
    }
}
